public class ElectricNumBean {
    private String hxf;
    private String wc;
    private String jx;
    private String pub;
    private String totalOfElec;
    private String time;

    public String getHxf() {
        return hxf;
    }

    public void setHxf(String hxf) {
        this.hxf = hxf;
    }

    public String getWc() {
        return wc;
    }

    public void setWc(String wc) {
        this.wc = wc;
    }

    public String getJx() {
        return jx;
    }

    public void setJx(String jx) {
        this.jx = jx;
    }

    public String getPub() {
        return pub;
    }

    public void setPub(String pub) {
        this.pub = pub;
    }

    public String getTotalOfElec() {
        return totalOfElec;
    }

    public void setTotalOfElec(String totalOfElec) {
        this.totalOfElec = totalOfElec;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
